# 依赖检测函数
check_env() {
    local missing=0
    echo -e "${pink_c}─────────────────────────── 运行环境检测 ─────────────────────────────${plain}"
    
    if ! command -v docker >/dev/null 2>&1; then
        red "  ❌ 未检测到 Docker！"
        missing=1
    else
        green "  ✅ Docker 已安装"
    fi
    
    if ! command -v nginx >/dev/null 2>&1; then
        red "  ❌ 未检测到 Nginx！"
        missing=1
    else
        green "  ✅ Nginx 已安装"
    fi
    
    if [ $missing -eq 1 ]; then
        echo -e "${pink_c}───────────────────────────────────────────────────────────────────${plain}"
        red "❌ 致命错误: 必须安装上述缺失的核心环境 (Docker / Nginx) 才能启动控制台！"
        yellow "💡 提示: 请先在服务器上安装缺失的依赖，然后再重新运行此脚本。"
        echo ""
        exit 1
    else
        green "✅ 运行环境检查通过！"
        sleep 1
    fi
}

